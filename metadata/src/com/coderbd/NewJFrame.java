    public void display() {
        DefaultTableModel model = (DefaultTableModel) tblDisplay.getModel();
        List<String> colsList = new ArrayList();
        colsList.add("ID");
        colsList.add("Name");
        colsList.add("Country");

        int colsNo = colsList.size();
        for (int i = 0; i < colsNo; i++) {
            model.addColumn(colsList.get(i).toString());
        }
        Object[] row;

        for (int j = 0; j < 100; j++) {
            int k = 0;
            row = new Object[colsNo];
            while (k < colsNo) {
                row[k] = j + " BD";
                k++;
            }
            model.addRow(row);
        }

    }

