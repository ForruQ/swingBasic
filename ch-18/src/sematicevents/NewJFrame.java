    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if (!checkEmailValidityNew(txtEmail.getText()) || txtEmail.getText().trim().length() < 1) {
            JOptionPane.showMessageDialog(null, "Enter valid Email");
        } else if (txtName.getText().trim().length() < 4) {
            JOptionPane.showMessageDialog(null, "Enter At least 5 Charcters");
        } else if (Integer.parseInt(txtAge.getText()) < 18 || Integer.parseInt(txtAge.getText()) > 100) {
            JOptionPane.showMessageDialog(null, "Enter Age Between 18 and 100");
        } else if (chkJava.isSelected() == false && chkSwing.isSelected() == false) {
            JOptionPane.showMessageDialog(null, "Check At least one");
        } else if (chkJava.isSelected() == false && chkSwing.isSelected() == false) {
            JOptionPane.showMessageDialog(null, "Check At least one");
        } else if (rMale.isSelected() == false && rFemale.isSelected() == false) {
            JOptionPane.showMessageDialog(null, "Check Gender");
        } else if (cmbRound.getItemAt(cmbRound.getSelectedIndex()).equalsIgnoreCase("Select A Round")) {
            JOptionPane.showMessageDialog(null, "Seelct A Round");
        } else {
            System.out.println(txtEmail.getText());
            System.out.println(txtName.getText());
            System.out.println(txtAge.getText());
        }
    }//GEN-LAST:event_btnAddActionPerformed
    public boolean checkEmailValidityNew(String email) {

        int atpos = email.indexOf("@");
        int dotpos = email.lastIndexOf(".");

        if (atpos > 1 && (dotpos - atpos) > 2 && dotpos < email.length() - 2) {
            //  System.out.println("Email is valid");
            return true;
        } else {
            // System.out.println("Email is invalid");
            return false;
        }

    }

