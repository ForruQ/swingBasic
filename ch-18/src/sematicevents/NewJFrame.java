    private void txtOnlyCharKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtOnlyCharKeyTyped
        char c = evt.getKeyChar();
        if (!(Character.isAlphabetic(c) || (c == KeyEvent.VK_BACK_SPACE) || c == KeyEvent.VK_DELETE)) {
            evt.consume();
            JOptionPane.showMessageDialog(null, "Numbers Not Allowed!");
        }
    }//GEN-LAST:event_txtOnlyCharKeyTyped

    private void txtQtyKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtQtyKeyReleased
        // TODO add your handling code here:
        try {
            int qty = Integer.parseInt(txtQty.getText());
            double unitPrice = Double.parseDouble(txtUnitPrice.getText());
            double total = qty * unitPrice;
            txtTotal.setText(String.valueOf(total));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Provide Necessary Inputs");
        }

    }//GEN-LAST:event_txtQtyKeyReleased

    private void txtOnlyNumberKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtOnlyNumberKeyReleased
        char intVal = evt.getKeyChar();
        if (!(Character.isDigit(intVal) || (intVal == KeyEvent.VK_BACK_SPACE) || intVal == KeyEvent.VK_DELETE)) {
            evt.consume();
            JOptionPane.showMessageDialog(null, "Character Not Allowed!");
        }
    }//GEN-LAST:event_txtOnlyNumberKeyReleased

